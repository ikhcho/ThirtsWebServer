package com.thirts.speed;

public class SpeedVo {
	private int max_v;
	private int average_v;
	private int distance;
	private int time;
	private String score;
	private String falldown;
	private String speed;
	private String gyro;
	private String beacon;
	private String location;
	private String macaddress;
	private String date;
	
	public int getMax_v() {
		return max_v;
	}
	public void setMax_v(int max_v) {
		this.max_v = max_v; 
	}
	public int getAverage_v() {
		return average_v;
	}
	public void setAverage_v(int average_v) {
		this.average_v = average_v;
	}
	public int getDistance() {
		return distance;
	}
	public void setDistance(int distance) {
		this.distance = distance;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	public String getFalldown() {
		return falldown;
	}
	public void setFalldown(String falldown) {
		this.falldown = falldown;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getGyro() {
		return gyro;
	}
	public void setGyro(String gyro) {
		this.gyro = gyro;
	}
	public String getBeacon() {
		return beacon;
	}
	public void setBeacon(String beacon) {
		this.beacon = beacon;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getMacaddress() {
		return macaddress;
	}
	public void setMacaddress(String macaddress) {
		this.macaddress = macaddress;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
